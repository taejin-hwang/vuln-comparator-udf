        expect(parse('1!1.0.dev456')).to.not.equal(null);
        expect(parse('1!1.0a1')).to.not.equal(null);
        expect(parse('1!1.0a2.dev456')).to.not.equal(null);
        expect(parse('1!1.0a12.dev456')).to.not.equal(null);
        expect(parse('1!1.0a12')).to.not.equal(null);
